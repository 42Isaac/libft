#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int			i;
	const char	*push;

	i = 0;
	while (s[i] && s[i] != (char)c)
		i++;
	if (s[i] != (char)c)
		return (NULL);
	push = &s[i];
	return ((char *)push);
}