#include "../libft.h"

int		ft_atoi(const char *s)
{
	int i;
	int sign;
	int push;

	i = 0;
	sign = 1;
	push = 0;
	while (s[i] == ' ' ||
			s[i] == '\t' ||
			s[i] == '\r' ||
			s[i] == '\n' ||
			s[i] == '\v' ||
			s[i] == '\f')
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] && s[i] >= '0' && s[i] <= '9')
	{
		push = push * 10 + s[i] - '0';
		i++;
	}
	return (push * sign);
}
