#include "../libft.h"

char	*ft_strdup(const char *s1)
{
	char	*r;
	int		i;

	i = 0;
	while (s1[i])
		i++;
	if (!(r = ft_strnew(i)))
		return (NULL);
	while (i >= 0)
	{
		r[i] = s1[i];
		i--;
	}
	return (r);
}
