#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int			i;
	const char	*push;

	i = 0;
	while (s[i])
		i++;
	while (i > 0 && s[i] != (char)c)
		i--;
	if (s[i] != (char)c)
		return (NULL);
	push = &s[i];
	return ((char *)push);
}
